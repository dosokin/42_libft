int	ft_atoi(const char *nptr)
{
	int		i;
	int		sign;
	int		r;

	r = 0;
	sign = 1;
	i = 0;
	while (nptr[i] == 32 || (nptr[i] >= 9 && nptr[i] <= 13))
		i++;
	if (nptr[i] == '+' || nptr[i] == '-')
	{
		if (nptr[i] == '-')
			sign = -sign;
		i++;
	}
	while (nptr[i] >= 48 && nptr[i] <= 57)
	{
		r = r * 10 + (nptr[i] - '0');
		i++;
	}
	return (r * sign);
}
