#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		i;
	t_list	*addy;

	if (lst == NULL)
		return (0);
	i = 1;
	addy = lst->next;
	while (addy)
	{
		addy = addy->next;
		i++;
	}
	return (i);
}
